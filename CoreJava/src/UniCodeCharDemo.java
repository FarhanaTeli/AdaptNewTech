
public class UniCodeCharDemo {

	public static void main(String[] args) {
		//ASCII Code for a to z(097-122) and ASCII Code for A to Z(065-090)
				char var='A';
				System.out.println("0:"+var);
				
				char var1=69;
				System.out.println("1:"+var1);
				
				char var2=121;
				System.out.println("2:"+var2);
				
				char var3=125;
				System.out.println("3:"+var3);
				
				char var4='\u00A7';
				System.out.println("4:"+var4);
				
				char var5='\u20AC';
				System.out.println("5:"+var5);

	}

}
